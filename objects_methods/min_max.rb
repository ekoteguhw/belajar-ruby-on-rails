# min_max.rb

=begin
.min is a method that will return the minimum value in an array.
.max is a method that will return the maximum value in an array.
.minmax is a method that will return the minimum and maximum value from an array in the form of array.
=end

arr = [1, 4, 9, 5, 10, 3, 6]

puts "Min value is #{arr.min}\n"
puts "Max value is #{arr.max}\n"
puts "MinMax value is #{arr.minmax}"
